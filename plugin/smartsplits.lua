vim.pack.add(require("load").gh({ "mrjones2014/smart-splits.nvim" }))
require("smart-splits").setup({})

vim.keymap.set("n", "<A-h>", function()
    require("smart-splits").resize_left()
end, { nnoremap = true, desc = "smart-splits - resize left" })
vim.keymap.set("n", "<A-j>", function()
    require("smart-splits").resize_down()
end, { nnoremap = true, desc = "smart-splits - resize down" })
vim.keymap.set("n", "<A-k>", function()
    require("smart-splits").resize_up()
end, { nnoremap = true, desc = "smart-splits - resize up" })
vim.keymap.set("n", "<A-l>", function()
    require("smart-splits").resize_right()
end, { nnoremap = true, desc = "smart-splits - resize right" })
