    require("smart-splits").move_cursor_left()
end, { nnoremap = true, desc = "smart-splits - move cursor left" })
vim.keymap.set("n", "<C-j>", function()
    require("smart-splits").move_cursor_down()
end, { nnoremap = true, desc = "smart-splits - move cursor down" })
vim.keymap.set("n", "<C-k>", function()
    require("smart-splits").move_cursor_up()
end, { nnoremap = true, desc = "smart-splits - move cursor up" })
vim.keymap.set("n", "<C-l>", function()
    require("smart-splits").move_cursor_right()
end, { nnoremap = true, desc = "smart-splits - move cursor right" })
